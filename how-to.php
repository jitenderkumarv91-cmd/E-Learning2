    <h2>How to Add CSS</h2>

    <p>
        There are three different ways to apply CSS to HTML documents.
    </p>

    <h3>1. Inline CSS</h3>
    <p>
        Inline CSS is written directly inside an HTML element.
    </p>
    <pre>
&lt;h1 style="color:red;"&gt;Hello&lt;/h1&gt;
    </pre>

    <h3>2. Internal CSS</h3>
    <p>
        Internal CSS is written inside the &lt;style&gt; tag
        in the HTML &lt;head&gt; section.
    </p>

    <h3>3. External CSS</h3>
    <p>
        External CSS is written in a separate CSS file
        and linked to HTML.
        This is the most recommended method.
    </p>
