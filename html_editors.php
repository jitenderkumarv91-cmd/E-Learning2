        <h2>Learn HTML Using Notepad or TextEdit:-</h2>
        <ul>
            <li>Web pages can be created and modified by using professional HTML editors.</li>
            <li>However, for learning HTML we recommend a simple text editor like Notepad (PC) or TextEdit (Mac).</li>
            <li>We believe that using a simple text editor is a good way to learn HTML.</li>
            <li>Follow the steps below to create your first web page with Notepad or TextEdit.</li>
        </ul>
        <h1>Step 1: Open Notepad (PC)</h1>
    <b>Windows 8 or later:</b> <br/>

    Open the <b>Start Screen</b> (the window symbol at the bottom left on your screen). <b>Type Notepad</b>.<br/>
    <b>
    Windows 7 or earlier:</b><br/>

    Open Start > Programs > Accessories > Notepad

    <h1>Step 1: Open TextEdit (mac)</h1>
    Open<b>Finder > Applications > TextEdit</b> <br/>

    Also change some preferences to get the application to save files correctly.In <b>Preferences > Format ></b> choose <b>"Plain Text"</b><br/>

    Then under "Open and Save", check the box that says "Display HTML files as HTML code instead of formatted text".<br/>

    <b>Then open a new document to place the code.</b>
